export function getAmbiente(slug) {
  return ambientes.find((a) => a.slug === slug)
}

export function getProjeto(ambSlug, projSlug) {
  const amb = getAmbiente(ambSlug)
  if (!amb) return null
  const projeto = amb.projetos.find((p) => p.slug === projSlug)
  return projeto ? { ambiente: amb, projeto } : null
}
